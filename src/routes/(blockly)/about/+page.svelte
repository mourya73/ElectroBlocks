                    more. The platform also includes real-time simulations, helping students visualize circuit wiring
                    alongside code execution for a more interactive learning experience.</p>

            <p>ElectroBlocks is an open-source platform that promotes electronics and programming education. It is designed to be used in schools, coding clubs, and self-learning environments worldwide.</p>

